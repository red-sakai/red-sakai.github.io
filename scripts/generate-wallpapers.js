const fs = require('fs');
const path = require('path');

const wallpapersDir = path.resolve(__dirname, '../public/wallpapers');
const outputFile = path.resolve(__dirname, '../data/wallpapers-generated.ts');

const files = fs.readdirSync(wallpapersDir)
  .filter(f => f !== '.gitkeep')
  .filter(f => /\.(jpg|jpeg|png|gif|webp|bmp|svg)$/i.test(f))
  .sort();

const wallpapers = files.map((f, i) => {
  const ext = path.extname(f);
  const name = path.basename(f, ext)
    .replace(/[-_]/g, ' ')
    .replace(/\b\w/g, c => c.toUpperCase());
  return `  { id: "wp-${i + 1}", label: ${JSON.stringify(name)}, path: "/wallpapers/${f}" }`;
});

const content = `// Auto-generated by scripts/generate-wallpapers.js — do not edit manually
export const WALLPAPER_PRESETS = [
${wallpapers.join(',\n')}
];
`;

fs.writeFileSync(outputFile, content, 'utf-8');
console.log(`Generated ${wallpapers.length} wallpapers → ${outputFile}`);
